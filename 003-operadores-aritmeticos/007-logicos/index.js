// O operador && (e/and) retorna true se os dois operandos for true

console.log(1 === 1 && 2 >= 1);
console.log(1 === 1 && 1 >= 2);
console.log("-------------");

// O operador || (ou/or) retorna true se um dos operandos for true

console.log(1 === 2 || 1 === 1);
console.log("-------------");

// O operador ! (não/not) faz uma negação

console.log(!true);
console.log(!false);