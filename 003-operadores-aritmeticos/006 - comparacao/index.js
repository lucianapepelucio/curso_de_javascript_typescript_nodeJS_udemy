// O operador == compara somente o valor

console.log(1 == 1);
console.log(1 == "1");
console.log("-------------");

// O operador === compara o valor e o tipo

console.log(1 === "1");
console.log(1 === 1);
console.log("-------------");

// O operador != compara somente o valor

console.log(1 != 1);
console.log(1 != "1");
console.log("-------------");

// O operador !== compara o valor e o tipo

console.log(1 !== "1");
console.log(1 !== 1);
console.log("-------------");

// Operador >

console.log(2 > 3);
console.log(3 > 2);
console.log("-------------");

// Operador <

console.log(2 < 3);
console.log(3 < 2);
console.log("-------------");

// Operador >=

console.log(1 >= 1);
console.log(3 >= 2);
console.log("-------------");

// Operador <=

console.log(1 <= 1);
console.log(2 <= 3);
console.log("-------------");